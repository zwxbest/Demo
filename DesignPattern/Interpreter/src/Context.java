public class Context
{
	private String input;
	public final String getInput()
	{
		return input;
	}
	public final void setInput(String value)
	{
		input = value;
	}

	private String output;
	public final String getOutput()
	{
		return output;
	}
	public final void setOutput(String value)
	{
		output = value;
	}
}